public class whileLoops {
    public static void main(String[] args) {
       int value = 5;
       while(value>0){
           System.out.println("Stop"+" "+value);
           value = value-1;
       }
       while(value==0){
           System.out.println("Go"+" "+value);
           value = value +1;
       }
    }
}
